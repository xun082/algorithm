/**
 * @param {string} s
 * @return {boolean}
 */
var validPalindrome = function (s) {
  function isPalindrome(left, right) {
    while (left < right) {
      if (s[left] !== s[right]) {
        return false;
      }
      left++;
      right--;
    }
    return true;
  }

  let left = 0;
  let right = s.length - 1;

  while (left < right) {
    if (s[left] !== s[right]) {
      // 删除其中一个,只要有一个符合即可
      return isPalindrome(left + 1, right) || isPalindrome(left, right - 1);
    }
    // 相等情况下
    left++;
    right--;
  }

  return true;
};
